package com.bean;

public class RegStudBean {
	private String stud_uname;
	private String stud_fname;
	private String stud_lname;
	private String stud_mail;
	private String stud_pass;
	public String getStud_uname() {
		return stud_uname;
	}
	public void setStud_uname(String stud_uname) {
		this.stud_uname = stud_uname;
	}
	public String getStud_fname() {
		return stud_fname;
	}
	public void setStud_fname(String stud_fname) {
		this.stud_fname = stud_fname;
	}
	public String getStud_lname() {
		return stud_lname;
	}
	public void setStud_lname(String stud_lname) {
		this.stud_lname = stud_lname;
	}
	public String getStud_mail() {
		return stud_mail;
	}
	public void setStud_mail(String stud_mail) {
		this.stud_mail = stud_mail;
	}
	public String getStud_pass() {
		return stud_pass;
	}
	public void setStud_pass(String stud_pass) {
		this.stud_pass = stud_pass;
	}

}
