package com.bean;

public class RegLectBean {
	private String lect_fname;
	private String lect_lname;
	private String lect_mail;
	private String lect_pass;
	public String getLect_fname() {
		return lect_fname;
	}
	public void setLect_fname(String lect_fname) {
		this.lect_fname = lect_fname;
	}
	public String getLect_lname() {
		return lect_lname;
	}
	public void setLect_lname(String lect_lname) {
		this.lect_lname = lect_lname;
	}
	public String getLect_mail() {
		return lect_mail;
	}
	public void setLect_mail(String lect_mail) {
		this.lect_mail = lect_mail;
	}
	public String getLect_pass() {
		return lect_pass;
	}
	public void setLect_pass(String lect_pass) {
		this.lect_pass = lect_pass;
	}
	
}
